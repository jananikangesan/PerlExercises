# Perl Program to illustrate the Logical Operators
 
# Operands
$a = true;
$b = false;
 
# AND operator
$result1 = $a and $b;
print "AND Operator: ", $result1,"\n";
  
# OR operator
$result2 = $a or $b;
print "OR Operator: ", $result2,"\n";
  
# NOT operator
$d = 0;
$result3 = not($d);
print "NOT Operator: ", $result3;